//Arduino头文件
#include <Arduino.h>

//硬件驱动
#include "beep.h"

/*
************************************************************
*   函数名称： Beep_Init
*
*   函数功能： 蜂鸣器IO初始化
*
*   入口参数： 无
*
*   返回参数： 无
*
*   说明：   
************************************************************
*/
void BEEP_CLASS::Beep_Init(void)
{
  
    pinMode(BEEP_PIN, OUTPUT);  
  
}

/*
************************************************************
*   函数名称： Beep_Set
*
*   函数功能： 蜂鸣器控制
*
*   入口参数： status：BEEP_ON-鸣叫   BEEP_OFF-关闭
*
*   返回参数： 无
*
*   说明：   
************************************************************
*/
void BEEP_CLASS::Beep_Set(bool status)
{
  
    digitalWrite(BEEP_PIN, status);
    beepSta = status;
  
}

